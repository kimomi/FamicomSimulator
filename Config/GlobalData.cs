            public byte R;
            public byte G;
            public byte B;
            public byte A;

            public PaletteData(byte r, byte g, byte b, byte a)
            {
                R = r;
                G = g;
                B = b;
                A = a;
            }

            public uint ToUint()
            {
                return ((uint)A << 24) | ((uint)(B << 16)) | ((uint)(G << 8)) | ((uint)R);
            }
